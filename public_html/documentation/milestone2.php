			<h2>Introduction</h2>

			<p>Below is a proposed persona and use case for Reb Willard&#146;s web site. This site is meant to serve as
				a gallery for her works to help increase her sales and establish a professional social media presence.</p>

			<h2>Persona</h2>

			<p><strong>Name:</strong> Nancy Reid</p>

			<p><strong>Age:</strong> 55</p>

			<p><strong>Profession:</strong> Upper level manager at Sandia National Labs in change of communication inside
				and outside the facility. Nancy works hard, and enjoys a rich personal life with her family and friends. She
				enjoys fine dining, art and fashion. She is well known in the community because of her work in the media and her philanthrop. She socializes often with friends. She has a home in Albuquerque that is beautifully decorated and cabin in Taos
				that she uses when she and her family go skiing 5 or 6 times a year.</p>
			<p><strong>Technology:</strong> At work Nancy has uses a PC running Windows 8.1. She works with the Microsoft Office Suite and
				other proprietary publishing software. Her browser is the latest version of Internet Explorer 11. At home Nancy owns a 21.5-inch iMac 1.4GHz dual-core Intel Core i5 and runs Safari for her browser.
				She likes the iMac for its styling and the larger screen. Nancy owns a iPhone, but prefers to browse the internet on
				a desktop because of her poor eyesight. Nancy is not especially technical, but has learned the programs
				needed for her work and hobbies.</p>

			<p><strong>Attitudes and Behaviors: </strong> Nancy loves fashion and art. she enjoys spending time on the
				internet collecting. eBay is one of her favorite sites. She likes to watch for luxury brands and only bids
				on the the best items for which she is willing to pay more.</p>

			<p><strong>Frustrations and Needs:</strong> In addition to the time Nancy spends &#147;collecting&#148; online,
				she also
				enjoys browsing for new artist and works of art. There is always room in her collection for a new piece.
				Nancy finds that many of the gallery and artist websites are not very appealing.</p>

			<p><strong>Goals:</strong> Nancy&#146;s goal is to sit down this evening with a nice glass of wine and peruse the web
				for new works of art. She has nothing specific in mind just 45 minutes of relaxing and browsing before
				dinner. Our goal is to catch her attention with Reb Willard&#146;s site, so we can capture her contact information for future communication and to increase the chaces of a future sale.</p>
