			<h2>Use Case</h2>

			<p>Nancy has the evening free and is planning on opening a nice bottle of wine, relax and peruse the web for
				new artists that strike her fancy. Nancy is viewing the site on her 21.5-inch iMac at home using Safari.
			</p>
			<ol>
				<li>Nancy lands on Home page. The site is simple and she enjoys the image of the painting. She decides to
					investigates further.
				</li>
				<li>She clicks on the Selected Works tab.</li>
				<li>She scrolls through a number of Reb&#146;s works enjoying the acrylics and photographs.</li>
				<li>From the selected works page Nancy navigates to the About Reb page. She really enjoys the bio and can
					relate to Reb so moves to the Connect page.
				</li>
				<li>Where she fills out the form to be emailed the periodic newsletter.</li>
			</ol>

			<h2>Proposed Site Map</h2>

			<p>Site will consist of 4 top-level pages total. No sub-pages or sub-navigation. This will be the Minimum
				Viable Product.</p>
			<ul>
				<li>Home Page</li>
				<li>Selected Works</li>
				<li>About Reb</li>
				<li>Connect</li>
			</ul>
			<p><strong>Home Page:</strong> A page filled with a close up of one of Reb’s works. Possibly a slide show of a
				few painting and/or a quote from the artist. The navigation bar at the top and social media links in the footer.</p>

			<p><strong>Selected Work:</strong> 8 - 10 examples of Reb’s acrylics and photographs works.</p>

			<p><strong>About Reb:</strong> This page will have a picture of Reb’s with a bio and a paragraph about her as an artist.</p>

			<p><strong>Contact:</strong> This page will contain and interesting image, a quote from Reb and a simple contact form to capture the information on those that are interested in her work.
			<p><strong>Note:</strong> Every page will have social media connections in the footer.</p>

